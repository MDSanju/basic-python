# Calculate plane shape of area

# Triangle
base = input("Enter Base = ")
height = input("Enter Height = ")

tr_base = float(base)
tr_height = float(height)

area = 0.5 * tr_base * tr_height

print("Area of a Triangle", area)


# Area of a circle
radius = float(input("Enter Radius = "))

area_of_radius = 3.1416 * radius * radius

print("Area of a Circle : ", area_of_radius)